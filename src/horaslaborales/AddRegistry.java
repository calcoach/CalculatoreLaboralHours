    private void showData(Calculator calc) {

        if (jTable1.getSize().height > 0) {

        }

        double[] costo = calc.calcularSueldo();
        calc.contT = 0;
        int[] horas = calc.calcularHoras();
        DefaultTableModel table = (DefaultTableModel) this.jTable1.getModel();
        table.setNumRows(0);

        int n = horas.length;

        for (int i = 0; i < n; i++) {

            if (horas[i] != 0) {

                //Mostrar primero info domingo o festivo
                if (n > 5 & verifiedRegistry.selectTypeDay() == 4) {
                    if (i < 5) {

                        if (i != 4) {
                            table.addRow(new Object[]{nameHours(i + 5), horas[i], costo[i]});
                        } else
                            table.addRow(new Object[]{nameHours(i), horas[i], costo[i]});

                    } else {
                        if (i < 9) {
                            table.addRow(new Object[]{nameHours(i - 5), horas[i], costo[i]});
                        } else {
                            table.addRow(new Object[]{nameHours(i), horas[i], costo[i]});
                        }

                    }

                    //Mostrar info turno de dos dias ordinario
                } else if (getTimeChooser(time2) < getTimeChooser(time1) & verifiedRegistry.selectTypeDay() == 1) {

                    if (i > 4 & i < 9) {
                        table.addRow(new Object[]{nameHours(i - 5), horas[i], costo[i]});
                    } else {
                        table.addRow(new Object[]{nameHours(i), horas[i], costo[i]});
                    }

                } else {

                    table.addRow(new Object[]{nameHours(i), horas[i], costo[i]});
                }
            }

        }

    }

