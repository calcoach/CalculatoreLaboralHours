    private int getTimeChooser(JComboBox combo) {
        int select = combo.getSelectedIndex();

        if (select == 0) {
            if (verifiedRegistry.selectTypeDay() == 3 | verifiedRegistry.selectTypeDay() == 4) {
                return 0;
            } else {
                return 24;
            }
        } else {
            return select;
        }
    }

